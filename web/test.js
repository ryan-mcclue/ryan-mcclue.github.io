function get_dimensions_conforming_to_aspect_ratio(buf, window_width, window_height) {
  let result = new Rect();

  let optimal_window_width = (buf.width / buf.height) * window_height;
  let optimal_window_height = (buf.height / buf.width) * window_width;

  if (optimal_window_width > window_width) {
    // NOTE(Ryan): width constrained, top and bottom black bars
    result.min_x = 0;
    result.max_x = window_width;

    let empty_space = window_height - optimal_window_height;
    let half_empty = Math.round(0.5 * empty_space);
    let use_height = Math.round(optimal_window_height);

    result.min_y = half_empty;
    result.max_y = result.min_y + use_height;
  } else {
    result.min_y = 0;
    result.max_y = window_height;

    let empty_space = window_width - optimal_window_width;
    let half_empty = Math.round(0.5 * empty_space);
    let use_width = Math.round(optimal_window_width);

    result.min_x = half_empty;
    result.max_x = result.min_x + use_width;
  }

  return result;
}

